if [ "$SCAM_COUNT" -gt 0 ]; then
  echo "в”Ѓв”Ѓв”Ѓ Scam Interactions в”Ѓв”Ѓв”Ѓ"
  echo "$RESPONSE" | jq -r '.scam_interactions[] | 
    "  рџ”ґ \(.token_name) (\(.token_address[0:10]...)) вЂ” \(.type) on \(.date)"'
  echo ""
