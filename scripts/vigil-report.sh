  "  \(if .risk == "critical" then "рџ”ґ" elif .risk == "high" then "рџџ " else "рџџЎ" end) \(.token_symbol) в†’ \(.spender_name // .spender_address[0:16])\n     Amount: \(if .amount == "unlimited" then "UNLIMITED вљ пёЏ" else .amount end)"'
